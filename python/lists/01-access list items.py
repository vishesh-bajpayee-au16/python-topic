# ACCESS ITEMS 

the_list = ["apple","banana","mango","orange"]
print(the_list[1])

# NEGATIVE INDEXING 
print(the_list[-1])

# RANGE OF INDEXES
# search startes at 2 index and ends at 3 excluding the 4th index
print(the_list[2:4])
# leave end value, then search will go till the end o the list
print(the_list[2:])

# CHECK IF ITEM EXISTS IN LIST
print("apple" in the_list)
