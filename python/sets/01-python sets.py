# A set is a collection which is both unordered and unindexed

thiset = {"apple","banana","mango"}
print(thiset)

# no duplicates allowed 
thiset = {"apple","apple","banana","mango"}
print(thiset)

# get length of set
print(len(thiset))

# Set can be used with any datatypes 
set3 = {True, False, False}
print(set3)
